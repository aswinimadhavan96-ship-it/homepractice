class Alloperators
{
		public static void main(String[] args)
		{
			int a=5;
			int b=15;
			int c=b/a;
			int d=b%a;
			int e=a+=5;
			boolean f=b>a;
			boolean g=(a!=b);
			System.out.println("DIVISION =" +c);
			System.out.println("REMAINDER=" +d);
			System.out.println("DUPLICATE=" +e);
			System.out.println("SMALLER=" +f);
			System.out.println("NOT EQUAL=" +g);
			System.out.print("B>A=");
			System.out.println((a==b)||(a<b));
			
		
		}
}